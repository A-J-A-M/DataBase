    private void btnRegActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnRegActionPerformed
    {//GEN-HEADEREND:event_btnRegActionPerformed
        try
        {
            if (Integer.parseInt(txtSalario.getText()) > 0 && Integer.parseInt(txtSalario.getText()) < 5000)
            {
                int a = Integer.parseInt(txtCve.getText());
                String b = txtNombre.getText();
                String c = txtApPa.getText();
                String d = txtApMa.getText();
                String e = txtCurp.getText();
                String f = txtRfc.getText();
                double g = Double.parseDouble(txtSalario.getText());
                String h = txtLic.getText();
                String i = cBoxUGE.getSelectedItem().toString();
                String j = txtCargo.getText();
                boolean xd = Prb.conn.insertaProf(a, b, c, d, e, f, g, h, i, j,this);
                if (xd)
                {
                    Mensaje.exito(this, "Se ha insertado el profesor con √©xito");
                    CtrlInterfaz.limpia(txtCve,txtNombre,txtApPa,txtApMa,txtCurp,txtRfc,txtSalario,txtLic,txtCargo);
                }
            }
            else
            {
                Mensaje.error(this, "Salario no valido. Debe ser menor a 5000");
            }
        } catch (Exception e)
        {
            Mensaje.error(this, "Error en la captura");
        }
    }//GEN-LAST:event_btnRegActionPerformed

